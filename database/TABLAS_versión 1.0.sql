--TABLA DE ROLES
CREATE TABLE ROLES (
    id SERIAL PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE CHECK (nombre IN ('JEFE', 'TRABAJADOR'))
);

