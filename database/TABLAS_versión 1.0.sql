--TABLA DE USUARIOS
CREATE TABLE USUARIOS (
    id SERIAL PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    correo VARCHAR(50) NOT NULL UNIQUE,
    contrasena VARCHAR(60) NOT NULL,
    id_Rol INT NOT NULL,
    FOREIGN KEY (id_Rol) REFERENCES ROLES(id)
);

